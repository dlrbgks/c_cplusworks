#include<iostream>
#include<string>
#include<map>
using namespace std;

int main() {
	map<string, string> dict;
	
	dict.insert({ "이진수", "컴퓨터가 사용하는 0과 1로 이루어진 수" });
	dict.insert({ "비트","정보 기술에서 데이터의 가장 작은 단위로, 0 또는 1의 값을 가진다" });
	dict.insert({ "컴파일", "프로그래밍 언어로 작성된 소스 코드를 컴퓨터가 이해하고 실행가능한 기계어로 변환하는 과정" });
	
	cout << dict["비트"] << endl;
	
	auto it = dict.find("컴파일");
	cout << it->first << " : " << it->second << endl;

	string eng;
	while (true) {
		cout << "검색할 단어 입력(exit - 종료)>>";
		getline(cin, eng);
		if (eng == "exit") {
			break;
		}
		else if (dict.find(eng) == dict.end()) {
			cout << "찾는 단어가 없습니다" << endl;
		}
		else {
			cout << dict[eng] << endl;
		}
	}
	cout << "검색을 종료합니다" << endl;





	return 0;
}